#!/usr/bin/env node
// Verifier orchestration for app-OME-FEAT-001 (TS-native, no Reward Kit).
// Runs in the agent's container after the agent/oracle finishes:
//   1. edit guard (C-REUSE-1)   2. PASS_TO_PASS   3. FAIL_TO_PASS (Playwright/HTTP)
//   4. rubric (AST/fs + judge)  5. assemble reward.json + artifact record
//
// Paths default to the in-container Harbor layout but are env-overridable for local runs.
import { execSync, spawn } from 'node:child_process'
import { readFileSync, writeFileSync, existsSync, mkdirSync, rmSync } from 'node:fs'
import { join } from 'node:path'
import { runProgrammaticRubric, judgeScopeCriterion } from './rubric.mjs'

const APP_DIR = process.env.OM_EVAL_APP_DIR || '/app/eval-app'
const LOG_DIR = process.env.OM_LOG_DIR || '/logs/verifier'
const ART_DIR = process.env.OM_ARTIFACT_DIR || '/logs/artifacts'
const BASELINE_DIR = process.env.OM_BASELINE_DIR || '/opt/evals'
const PORT = Number(process.env.OM_APP_PORT || 3123)
const BASE_URL = `http://127.0.0.1:${PORT}`
const INSTANCE_ID = 'open-mercato__app-OME-FEAT-001'
const PASS_THRESHOLD = 0.85

mkdirSync(LOG_DIR, { recursive: true })
mkdirSync(ART_DIR, { recursive: true })

const log = (...a) => console.error('[verify]', ...a)
function sh(cmd, opts = {}) {
  return execSync(cmd, { cwd: APP_DIR, encoding: 'utf8', env: process.env, stdio: 'pipe', maxBuffer: 64 * 1024 * 1024, ...opts })
}
function shTry(cmd, opts = {}) {
  try { return { ok: true, out: sh(cmd, opts) } }
  catch (e) { return { ok: false, out: `${e.stdout || ''}${e.stderr || ''}`, code: e.status ?? 1 } }
}

// ---------- 1. Edit guard (C-REUSE-1) ----------
function editGuard() {
  // Stage agent changes so new files are diffable, then list changed paths vs base.
  shTry(`git -C "${APP_DIR}" add -A`)
  const baseFile = join(BASELINE_DIR, 'base_commit')
  const base = existsSync(baseFile) ? readFileSync(baseFile, 'utf8').trim() : ''
  let filesChanged = []
  if (base) {
    const r = shTry(`git -C "${APP_DIR}" diff --cached --name-only ${base}`)
    filesChanged = r.out.split('\n').map((s) => s.trim()).filter(Boolean)
  }
  // @open-mercato package tamper check via content-hash manifest.
  let pkgUntouched = true
  let pkgNote = 'no baseline manifest'
  const manifest = join(BASELINE_DIR, 'om-packages.sha1')
  if (existsSync(manifest)) {
    const now = shTry(`cd "${APP_DIR}/node_modules" && find @open-mercato -type f -print0 | sort -z | xargs -0 sha1sum`)
    pkgUntouched = now.ok && now.out.trim() === readFileSync(manifest, 'utf8').trim()
    pkgNote = pkgUntouched ? '@open-mercato unchanged' : '@open-mercato package files modified'
  }
  // Git root IS eval-app, so every tracked change is within eval-app by construction.
  const score = pkgUntouched ? 1 : 0
  return { criterion: { id: 'C-REUSE-1', weight: 3, score, rationale: pkgNote }, filesChanged }
}

// ---------- 2. PASS_TO_PASS ----------
function passToPass() {
  const results = {}
  // build
  const build = shTry('yarn build')
  results['app build / typecheck (yarn build) exits 0'] = build.ok ? 'pass' : 'fail'
  if (!build.ok) log('build failed:\n', build.out.slice(-4000))
  // codegen runs cleanly. NOTE: in a standalone scaffold the generated output
  // (.mercato/, generated/, src/generated/) is gitignored and regenerated by
  // `yarn build` (build = "yarn generate && next build"), so a git "no diff"
  // check is meaningless — the meaningful signal is that generate exits 0.
  const gen = shTry('yarn generate')
  results['yarn generate produces no diff'] = gen.ok ? 'pass' : 'fail'
  if (!gen.ok) log('generate failed:\n', gen.out.slice(-3000))
  // pre-existing scaffold tests (best-effort: only if a test script exists)
  let testStatus = 'pass'
  const pkg = JSON.parse(readFileSync(join(APP_DIR, 'package.json'), 'utf8'))
  if (pkg.scripts && pkg.scripts.test) {
    const t = shTry('yarn test --passWithNoTests')
    testStatus = t.ok ? 'pass' : 'fail'
    if (!t.ok) log('scaffold tests failed:\n', t.out.slice(-3000))
  }
  results['pre-existing scaffold tests remain green'] = testStatus
  return results
}

// ---------- 3. FAIL_TO_PASS ----------
async function failToPass() {
  // Ensure schema + a known admin account exist (verifier owns provisioning).
  shTry('yarn db:migrate')
  const seed = shTry('yarn mercato auth setup --orgName Acme --orgSlug acme --email admin@acme.com --password secret --json')
  if (!seed.ok) log('auth setup note:', seed.out.slice(-800))

  // Stage the hidden suite + a self-contained playwright config under .eval/.
  const evalDir = join(APP_DIR, '.eval')
  mkdirSync(evalDir, { recursive: true })
  writeFileSync(join(evalDir, 'TC-BKM-001.spec.ts'), readFileSync(join('/tests', 'hidden', 'TC-BKM-001.spec.ts'), 'utf8'))
  writeFileSync(join(evalDir, 'playwright.config.ts'),
`import { defineConfig } from '@playwright/test'
export default defineConfig({
  testDir: '.',
  fullyParallel: false,
  retries: 1,
  reporter: [['json', { outputFile: 'pw-results.json' }]],
  timeout: 30000,
  use: { baseURL: process.env.BASE_URL },
})
`)

  // Boot the built app the way the scaffold does: `mercato server start`
  // (production mode, honors PORT). Not `next start` — the OM runtime wraps it.
  const serverEnv = { ...process.env, NODE_ENV: 'production', PORT: String(PORT) }
  const server = spawn('yarn', ['start'], {
    cwd: APP_DIR, env: serverEnv, stdio: 'pipe', detached: true,
  })
  server.stdout.on('data', () => {})
  server.stderr.on('data', () => {})
  const up = await waitForServer(BASE_URL + '/api/bookmarks', 90_000)

  const f2p = {}
  let pwOk = false
  if (!up) {
    log('app did not become ready')
  } else {
    const run = shTry(`BASE_URL='${BASE_URL}' npx playwright test --config .eval/playwright.config.ts`,
      { env: { ...process.env, BASE_URL } })
    pwOk = run.ok
    parsePlaywright(join(APP_DIR, 'pw-results.json'), f2p)
  }
  try { process.kill(-server.pid) } catch {}
  rmSync(evalDir, { recursive: true, force: true })
  rmSync(join(APP_DIR, 'pw-results.json'), { force: true })

  if (Object.keys(f2p).length === 0) {
    // Couldn't parse → record the canonical nodes as fail so we never silently pass.
    for (const n of CANONICAL_F2P) f2p[n] = pwOk ? 'pass' : 'fail'
  }
  return f2p
}

const CANONICAL_F2P = [
  'TC-BKM-001 :: POST /api/bookmarks {title,url,note} as authorized user -> 2xx, returns id',
  'TC-BKM-001 :: GET /api/bookmarks -> created row present with title/url/note echoed back',
  'TC-BKM-001 :: POST /api/bookmarks without note -> 2xx and note is null (optional)',
  'TC-BKM-001 :: POST /api/bookmarks with invalid url -> 400 validation error',
  'TC-BKM-001 :: GET /api/bookmarks with NO auth -> 401/403 (feature-gated)',
]

function parsePlaywright(file, out) {
  if (!existsSync(file)) return
  let json
  try { json = JSON.parse(readFileSync(file, 'utf8')) } catch { return }
  const walk = (suite, prefix) => {
    const title = [prefix, suite.title].filter(Boolean).join(' :: ')
    for (const spec of suite.specs || []) {
      const ok = (spec.tests || []).every((t) => (t.results || []).some((r) => r.status === 'passed'))
      out[`${title} :: ${spec.title}`] = ok ? 'pass' : 'fail'
    }
    for (const child of suite.suites || []) walk(child, title)
  }
  for (const s of json.suites || []) walk(s, '')
}

async function waitForServer(url, timeoutMs) {
  const deadline = Date.now() + timeoutMs
  while (Date.now() < deadline) {
    try {
      const res = await fetch(url, { method: 'GET' })
      if (res.status > 0) return true
    } catch {}
    await new Promise((r) => setTimeout(r, 1500))
  }
  return false
}

// ---------- 4 + 5. Rubric + assemble ----------
async function main() {
  const guard = editGuard()
  const p2p = passToPass()
  const f2p = await failToPass()

  const rubric = runProgrammaticRubric(APP_DIR)
  rubric.unshift(guard.criterion)
  const scope = await judgeScopeCriterion(APP_DIR, {
    apiKey: process.env.ANTHROPIC_API_KEY, model: process.env.OM_JUDGE_MODEL,
  })
  rubric.push(scope)

  const totalW = rubric.reduce((s, c) => s + c.weight, 0)
  const rubricScore = totalW ? rubric.reduce((s, c) => s + c.weight * c.score, 0) / totalW : 0

  const allF2P = Object.values(f2p).length > 0 && Object.values(f2p).every((v) => v === 'pass')
  const allP2P = Object.values(p2p).every((v) => v === 'pass')
  const passed = allF2P && allP2P && rubricScore >= PASS_THRESHOLD

  const f2pRate = rate(f2p)
  const p2pRate = rate(p2p)

  const rewards = {
    passed: passed ? 1 : 0,
    rubric_score: round(rubricScore),
    fail_to_pass_rate: round(f2pRate),
    pass_to_pass_rate: round(p2pRate),
  }
  for (const c of rubric) rewards[`criterion__${c.id}`] = c.score
  writeFileSync(join(LOG_DIR, 'reward.json'), JSON.stringify(rewards, null, 2))

  const record = {
    instance_id: INSTANCE_ID,
    agent: process.env.HARBOR_AGENT || process.env.OM_AGENT || 'unknown',
    run_id: process.env.HARBOR_RUN_ID || process.env.OM_RUN_ID || 'local',
    om_version: process.env.OM_VERSION || 'unknown',
    agent_cli_version: process.env.OM_AGENT_VERSION || 'unknown',
    model_id: process.env.OM_MODEL || 'unknown',
    judge_model_id: process.env.OM_JUDGE_MODEL || 'claude-opus-4-8',
    passed,
    fail_to_pass: f2p,
    pass_to_pass: p2p,
    files_changed: guard.filesChanged,
    rubric: rubric.map((c) => ({ id: c.id, score: c.score, weight: c.weight, rationale: c.rationale })),
    rubric_score: round(rubricScore),
    notes: '',
  }
  writeFileSync(join(ART_DIR, `${record.agent}-${record.run_id}.json`), JSON.stringify(record, null, 2))

  log('rewards:', JSON.stringify(rewards))
  log('verdict:', passed ? 'PASS' : 'FAIL')
}

const rate = (obj) => { const v = Object.values(obj); return v.length ? v.filter((x) => x === 'pass').length / v.length : 0 }
const round = (n) => Math.round(n * 1000) / 1000

main().catch((e) => {
  log('fatal:', e?.stack || String(e))
  try { writeFileSync(join(LOG_DIR, 'reward.txt'), '0') } catch {}
  process.exit(0)
})
