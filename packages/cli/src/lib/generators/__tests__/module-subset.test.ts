let tmpDir: string
let fileMtimeNonce = 0

function createTmpDir(): string {
  return fs.mkdtempSync(path.join(os.tmpdir(), 'module-subset-test-'))
}

function touchFile(filePath: string, content = ''): void {
  fs.mkdirSync(path.dirname(filePath), { recursive: true })
  fs.writeFileSync(filePath, content)
  const mtime = new Date(Date.now() + fileMtimeNonce)
  fileMtimeNonce += 1
  fs.utimesSync(filePath, mtime, mtime)
}

function scaffoldModule(
  tmpDir: string,
  modId: string,
  location: 'pkg' | 'app',
  files: string[]
): void {
  const base =
    location === 'pkg'
      ? path.join(tmpDir, 'packages', 'core', 'src', 'modules', modId)
      : path.join(tmpDir, 'app', 'src', 'modules', modId)
  for (const file of files) {
    touchFile(path.join(base, file))
  }
}

function createMockResolver(
  tmpDir: string,
  enabled: ModuleEntry[]
): PackageResolver {
  const outputDir = path.join(tmpDir, 'output', 'generated')
  fs.mkdirSync(outputDir, { recursive: true })

  return {
    isMonorepo: () => true,
    getRootDir: () => tmpDir,
    getAppDir: () => path.join(tmpDir, 'app'),
    getOutputDir: () => outputDir,
    getModulesConfigPath: () => path.join(tmpDir, 'app', 'src', 'modules.ts'),
    discoverPackages: () => [],
    loadEnabledModules: () => enabled,
    getModulePaths: (entry: ModuleEntry) => ({
      appBase: path.join(tmpDir, 'app', 'src', 'modules', entry.id),
      pkgBase: path.join(
        tmpDir,
        'packages',
        'core',
        'src',
        'modules',
        entry.id
      ),
    }),
    getModuleImportBase: (entry: ModuleEntry) => ({
      appBase: `@/modules/${entry.id}`,
      pkgBase: `@open-mercato/core/modules/${entry.id}`,
    }),
    getPackageOutputDir: () => outputDir,
    getPackageRoot: (from?: string) => {
      const packageName = from ?? '@open-mercato/core'
      if (packageName === '@open-mercato/core') {
        return path.join(tmpDir, 'packages', 'core')
      }
      return path.join(tmpDir, 'packages', packageName.replace(/^@open-mercato\//, ''))
    },
  }
}

function createStandaloneMockResolver(
  tmpDir: string,
  enabled: ModuleEntry[]
): PackageResolver {
  const outputDir = path.join(tmpDir, '.mercato', 'generated')
  fs.mkdirSync(outputDir, { recursive: true })

  return {
    isMonorepo: () => false,
    getRootDir: () => tmpDir,
    getAppDir: () => tmpDir,
    getOutputDir: () => outputDir,
    getModulesConfigPath: () => path.join(tmpDir, 'src', 'modules.ts'),
    discoverPackages: () => [],
    loadEnabledModules: () => enabled,
    getModulePaths: (entry: ModuleEntry) => ({
      appBase: path.join(tmpDir, 'src', 'modules', entry.id),
      pkgBase: path.join(
        tmpDir,
        'node_modules',
        'pkg',
        'dist',
        'modules',
        entry.id
      ),
    }),
    getModuleImportBase: (entry: ModuleEntry) => ({
      appBase: `@/modules/${entry.id}`,
      pkgBase: `@open-mercato/core/modules/${entry.id}`,
    }),
    getPackageOutputDir: () => outputDir,
    getPackageRoot: (from?: string) => path.join(tmpDir, 'node_modules', from ?? '@open-mercato/core'),
  }
}

function readGenerated(tmpDir: string, filename: string): string | null {
  const filePath = path.join(tmpDir, 'output', 'generated', filename)
  if (!fs.existsSync(filePath)) return null
  return fs.readFileSync(filePath, 'utf8')
}

